#include "ft_printf.h"

wchar_t	*ft_wstrcpy(wchar_t *dst, const wchar_t *src)
{
	size_t		i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
